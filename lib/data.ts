export function getPatientById(id: string): Patient | undefined {
  return mockPatients.find((p) => p.id === id)
}

export function getPatientsByDoctor(doctorName: string): Patient[] {
  return mockPatients.filter(
    (p) => p.doctorInCharge.toLowerCase() === doctorName.toLowerCase()
  )
}

export function getAllPatients(): Patient[] {
  return mockPatients
}

export function sortPatientsByRisk(patients: Patient[]): Patient[] {
  return [...patients].sort((a, b) => b.riskScore - a.riskScore)
}

export function getHighRiskPatients(): Patient[] {
  return mockPatients.filter((p) => p.riskScore >= 70)
}

export function getPatientStats() {
  const total = mockPatients.length
  const highRisk = mockPatients.filter((p) => p.riskScore >= 70).length
  const moderate = mockPatients.filter((p) => p.riskScore >= 40 && p.riskScore < 70).length
  const low = mockPatients.filter((p) => p.riskScore < 40).length
  
  return { total, highRisk, moderate, low }
}
